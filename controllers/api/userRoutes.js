
const router = require('express').Router();
const { User, Blog, Comment } = require('../../models');

// GET all users
router.get('/', (req, res) => {
    User.findAll({
        attributes: { exclude: ['password'] }
    })
        .then(dbUserData => res.json(dbUserData))
        .catch(err => {
            console.log(err);
            res.status(500).json(err);
        });
});

// GET a single user by ID
router.get('/:id', (req, res) => {
    User.findOne({
        attributes: {
            exclude: ['password']
        },
        where: {
            id: req.params.id
        },
        include: [
            {
                model: Blog,
                attributes: ['id', 'title', 'blog_content', 'created_at']
            },
            {
                model: Comment,
                attributes: ['id', 'blog_content', 'created_at'],
                include: {
                    model: Blog,
                    attributes: ['title']
                }
            }
        ]
    })
        .then(dbUserData => {
            if (!dbUserData) {
                res.status(404).json({ message: 'No user found with that ID!' });
                return;
            }
            res.json(dbUserData);
        })
        .catch(err => {
            console.log(err);
            res.status(500).json(err);
        });
});

// POST - Create a user
router.post('/', (req, res) => {
    User.create({
        username: req.body.username,
        email: req.body.email,
        password: req.body.password,
        twitter: req.body.twitter,
        github: req.body.github
    })
        .then(dbUserData => {
            req.session.save(() => {
                req.session.user_id = dbUserData.id;
                req.session.username = dbUserData.username;
                req.session.twitter = dbUserData.twitter;
                req.session.github = dbUserData.github;
                req.session.loggedIn = true;

                res.json(dbUserData);
            });
        });
});

// User login
router.post('/login', (req, res) => {
    User.findOne({
        where: {
            email: req.body.email
        }
    })
        .then(dbUserData => {
            if (!dbUserData) {
                res.json(400).json({ message: "That username doesn't exist!" });
                return;
            };

            const validPassword = dbUserData.passwordConfirm(req.body.password);

            if (!validPassword) {
                res.status(400).json({ message: "That's not the right password..." });
                return;
            }

            req.session.save(() => {
                req.session.user_id = dbUserData.id;
                req.session.username = dbUserData.username;
                req.session.twitter = dbUserData.twitter;
                req.session.github = dbUserData.github;
                req.session.loggedIn = true;

                res.json({ user: dbUserData, message: `Hi ${dbUserData.username}, you're logged in!` });
            });
        });
});

// User logout
router.post('/logout', (req, res) => {
    if (req.session.loggedIn) {
        req.session.destroy(() => {
            res.status(204).end();
        });
    } else {
        res.status(404).end();
    }
});

